package y2002.s4;

import java.io.DataInputStream;
import java.io.FileInputStream;
import java.io.IOException;
import java.util.ArrayList;
import java.util.Arrays;
import java.util.List;

/* Bridge Crossing
 * 100/100
 * Recursion, Logic
 * Full Solution

Essentially, there's a recursive run function that returns an array list
The first index is the total crossing time, and the rest are how many people
cross at each crossing.

Run takes a starting index, assuming we start from a fresh crossing.
Thus, in the run function, we loop through 1 - M people.

*/
public class Main {

    // --- variables
    static int M; // max people per crossing
    static int Q; // total people

    static String[] names; // input names
    static int[] times;    // input cross times

    // --- recursive call
    static List<Integer> run(int index, List<Integer> people) {
        if (index >= Q) return people;

        List<Integer> fastest = new ArrayList<>(Arrays.asList(Integer.MAX_VALUE));

        int currentSlowest = times[index];  // this run's slowest
        for (int m = 1; m <= M; m++) {
            if (m + index >= Q) break;
            if (times[m + index] <= currentSlowest) { // person is faster
                // nothing, person is added to crossing
            } else { // person is slower
                // move on to next cycle
                List<Integer> t = new ArrayList<>(people);
                t.set(0, people.get(0) + currentSlowest);
                t.add(m);
                List<Integer> temp = run(m + index, t);
                if (temp.get(0) < fastest.get(0)) {
                    fastest = temp;
                }

                // continue this one
                currentSlowest = times[m + index];
            }
        }

        // finally, move on to the next cycle with max
        // people crossing at this time.
        List<Integer> t = new ArrayList<>(people);
        t.set(0, people.get(0) + currentSlowest);
        if (Q - M >= index) {
            t.add(M); // max people
        } else {
            t.add(Q - index);
        }
        List<Integer> temp = run(index + M, t);
        if (temp.get(0) < fastest.get(0)) {
            fastest = temp;
        }

        return fastest;
    }

    // --- main
    public static void main(String[] args) throws IOException {
        FastReader reader = new FastReader();

        M = reader.nextInt();
        Q = reader.nextInt();

        names = new String[Q];
        times = new int[Q];

        for (int i = 0; i < Q; i++) {
            names[i] = reader.nextString();
            times[i] = reader.nextInt();
        }

        List<Integer> ret = run(0, new ArrayList<>(Arrays.asList(0)));

        int rT = ret.remove(0);
        List<Integer> rP = ret;

        System.out.println("Total Time: " + rT);
        int c = 0;
        for (int i = 0; i < rP.size(); i++) {
            for (int j = 0; j < rP.get(i); j++) {
                System.out.print(names[c++] + " ");
            }
            System.out.println();
        }
    }


    public static class FastReader {

        private final int BUFFER_SIZE = 1 << 16;
        private final DataInputStream din;

        private final byte[] buffer;
        private int bufferPointer, bytesRead;

        public FastReader() {
            din = new DataInputStream(System.in);
            buffer = new byte[BUFFER_SIZE];
            bufferPointer = bytesRead = 0;
        }

        public FastReader(String file_name) throws IOException {
            din = new DataInputStream(new FileInputStream(file_name));
            buffer = new byte[BUFFER_SIZE];
            bufferPointer = bytesRead = 0;
        }

        public String readLine() throws IOException {
            byte[] buf = new byte[6400]; // line length
            int cnt = 0, c;
            while ((c = read()) != -1) {
                if (c == '\n')
                    break;
                buf[cnt++] = (byte) c;
            }
            return new String(buf, 0, cnt);
        }

        public List<Integer> readLineAsIntegers() throws IOException {
//        int[] ret = new int[1024];
            List<Integer> ret = new ArrayList<>();
            int idx = 0;
            byte c = read();
            while (c != -1) {
                if (c == '\n' || c == '\r')
                    break;

                // next integer
                int i = 0;
                while (c <= ' ') {
                    c = read();
                }
                boolean negative = (c == '-');
                if (negative) {
                    c = read();
                }

                do {
                    i = i * 10 + (c - '0');
                    c = read();
                } while (c >= '0' && c <= '9');
//            ret[idx++] = (negative) ? -i : i;
                ret.add((negative) ? -i : i);
            }
            return ret;
        }

        public List<Long> readLineAsLongs() throws IOException {
            List<Long> ret = new ArrayList<>();
            int idx = 0;
            byte c = read();
            while (c != -1) {
                if (c == '\n' || c == '\r')
                    break;

                // next integer
                long i = 0;
                while (c <= ' ') {
                    c = read();
                }
                boolean negative = (c == '-');
                if (negative) {
                    c = read();
                }

                do {
                    i = i * 10 + (c - '0');
                    c = read();
                } while (c >= '0' && c <= '9');
                ret.add((negative) ? -i : i);
            }
            return ret;
        }

        public List<Double> readLineAsDoubles() throws IOException {
            List<Double> ret = new ArrayList<>();
            int idx = 0;
            byte c = read();
            while (c != -1) {
                if (c == '\n' || c == '\r')
                    break;

                // next integer
                double d = 0, div = 1;
                while (c <= ' ') {
                    c = read();
                }
                boolean negative = (c == '-');
                if (negative) {
                    c = read();
                }

                do {
                    d = d * 10 + (c - '0');
                    c = read();
                } while (c >= '0' && c <= '9');

                if (c == '.') {
                    while ((c = read()) >= '0' && c <= '9') {
                        d += (c - '0') / (div *= 10);
                    }
                }
                ret.add((negative) ? -d : d);
            }
            return ret;
        }

        public int nextInt() throws IOException {
            int ret = 0;
            byte c = read();
            while (c <= ' ')
                c = read();
            boolean neg = (c == '-');
            if (neg)
                c = read();
            do {
                ret = ret * 10 + c - '0';
            } while ((c = read()) >= '0' && c <= '9');

            return (neg) ? -ret : ret;
        }

        public long nextLong() throws IOException {
            long ret = 0;
            byte c = read();
            while (c <= ' ')
                c = read();
            boolean neg = (c == '-');
            if (neg) {
                c = read();
            }

            do {
                ret = ret * 10 + c - '0';
            }
            while ((c = read()) >= '0' && c <= '9');
            return (neg) ? -ret : ret;
        }

        public double nextDouble() throws IOException {
            double ret = 0, div = 1;
            byte c = read();
            while (c <= ' ') {
                c = read();
            }
            boolean neg = (c == '-');
            if (neg) {
                c = read();
            }
            do {
                ret = ret * 10 + c - '0';
            } while ((c = read()) >= '0' && c <= '9');

            if (c == '.') {
                while ((c = read()) >= '0' && c <= '9') {
                    ret += (c - '0') / (div *= 10);
                }
            }

            return (neg) ? -ret : ret;
        }

        public String nextString() throws IOException {
            byte[] ret = new byte[1024];
            int idx = 0;
            byte c = read();
            while (c <= ' ') {
                c = read();
            }
            do {
                ret[idx++] = c;
                c = read();
            } while (c != -1 && c != ' ' && c != '\n' && c != '\r');
            return new String(ret, 0, idx);

        }

        private byte read() throws IOException {
            if (bufferPointer == bytesRead) {
                // fill buffer
                bytesRead = din.read(buffer, bufferPointer = 0, BUFFER_SIZE);
                if (bytesRead == -1) {
                    buffer[0] = -1;
                }
            }
            return buffer[bufferPointer++];
        }

        public int[] readLineAsIntArray(int n) throws IOException {
            int[] ret = new int[n];
//            int ret = new ArrayList<>();
            int idx = 0;
            byte c = read();
            while (c != -1) {
                if (c == '\n' || c == '\r')
                    break;

                // next integer
                int i = 0;
                while (c <= ' ') {
                    c = read();
                }
                boolean negative = (c == '-');
                if (negative) {
                    c = read();
                }

                do {
                    i = i * 10 + (c - '0');
                    c = read();
                } while (c >= '0' && c <= '9');

                ret[idx++] = (negative) ? -i : i;
                if (idx >= n) {
                    break;
                }

//                ret.add((negative) ? -i : i);
            }
            return ret;
        }

        public void close() throws IOException {
            if (din != null) {
                din.close();
            }
        }

    }
}
